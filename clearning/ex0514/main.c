#include <stdio.h>

#define VALUE 8
#define VALUE2 2

int main()
{

    printf("The values are %d and %d\n", VALUE, VALUE2);
    printf("Addition: %d\n", VALUE+VALUE2);
    printf("Subtraction: %d\n", VALUE-VALUE2);
    printf("Multiplication: %d\n", VALUE*VALUE2);
    printf("Division: %d\n", VALUE/VALUE2);

    return(0);

}
